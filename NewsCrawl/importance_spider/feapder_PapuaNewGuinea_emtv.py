    def start_requests(self):
        for keyword in self.keywords:
            page = 1
            url = "https://emtv.com.pg/page/1/"
            params = {
                "s": f"{keyword}"
            }
            yield feapder.Request(url, params=params, callback=self.parse_url, page=page, keyword=keyword,
                                  filter_repeat=True)

